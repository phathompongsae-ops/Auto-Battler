ds = ImageDraw.Draw(sheet)
for i, (n, im) in enumerate(out.items()):
    cx = i * cw
    ds.rectangle([cx + 4, 4, cx + cw - 5, ch - 5],
                 fill=(52, 42, 32, 255), outline=(110, 92, 66, 255))
    sheet.paste(im, (cx + (cw - im.width) // 2, pad + (ch - 18 - pad * 2 - im.height) // 2 + pad), im)
